n1=int(input('First no: '))
n2=int(input('Second no: '))
o=input('Operator:')
if o=='-':
                print(n1-n2)
elif o=='+':
                print(n1+n2)
elif o=='*':
                print(n1*n2)
elif o=='%':
                print(n1%n2)
elif o=='/':
                print(n1/n2)
else:
                print('Wrong Input')
